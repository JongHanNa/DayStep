/**
 * MCP 서버용 ADHD 플래닝 가이드 프롬프트 생성
 */
export function generateAdhdPlanningPrompt(
  taskDescription: string,
  deadline?: string,
  todayFormatted?: string
): string {
  const deadlineText = deadline ? `\n마감일: ${deadline}` : '';
  const todayText = todayFormatted ? `\n오늘 날짜: ${todayFormatted}` : '';

  return `# 🧠 ADHD 친화적 계획 수립 가이드

목표: ${taskDescription}${deadlineText}${todayText}

---

${CAPS_METHOD_DETAILED}

${deadline ? `\n현재 마감일(${deadline})에서 역산해서 각 단계 기한을 정하세요.\n` : ''}
---

${PLANNING_QUESTIONS}

---

## 🛠️ 계획 생성 도구

플래닝이 완료되면 다음 도구로 DayStep에 등록하세요:

\`\`\`
create_project_with_todos
\`\`\`

**입력 예시:**
\`\`\`json
{
  "project": {
    "title": "${taskDescription}",
    "description": "AI 플래닝 결과",
    "icon": "🎯",
    "color": "#4ECDC4"
  },
  "todos": [
    {
      "title": "잡코리아에서 송도 IT 회사 5개 북마크하기",
      "start_time": "today",
      "schedule_type": "anytime",
      "priority": "high",
      "anytime_duration": 15
    },
    {
      "title": "이력서 템플릿 다운로드 받기",
      "start_time": "today",
      "schedule_type": "anytime",
      "priority": "medium",
      "anytime_duration": 10
    }
  ]
}
\`\`\`

---

${ADHD_TIPS_DETAILED}

---

자, 이제 "${taskDescription}"를 구체적인 행동들로 쪼개볼까요? 🚀`;
}

